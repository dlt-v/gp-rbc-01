# Działania na liczbach całkowitych i zmiennoprzecinkowych

a = 5
b = 2

print(f"Dodawanie: {a} + {b} = {a + b}")
print(f"Odejmowanie: {a} - {b} = {a - b}")
print(f"Mnożenie: {a} * {b} = {a * b}")
print(f"Dzielenie: {a} / {b} = {a / b}") # Wynikiem dzielenia jest zawsze Float

